import { badRequest, ok, serverError, unauthorized } from '../../../helpers/http/http-helper'
import { HttpRequest, Authentication } from './login-controller-protocols'
import { Validation } from '../../../../validation/validators'
import { AuthenticateModel } from '../../../../domain/usecases/authentication'

interface SutTypes {
  sut: LoginController
  authenticationStub: Authentication
  validationStub: Validation
}

const makeFakeRequest = (): HttpRequest => ({
  body: {
    email: 'any_email@mail.com',
    password: 'any_password'
  }
})

const makeAuthenticate = (): Authentication => {
  class AuthenticationStub implements Authentication {
    async auth (Authentication: AuthenticateModel): Promise<string> {
      return 'any_token'
    }
  }

  return new AuthenticationStub()
}

const makeValidation = (): Validation => {
  class ValidationStub implements Validation {
    validate (input: any): Error {
      return null
    }
  }

  return new ValidationStub()
}

const makeSut = (): SutTypes => {
  const authenticationStub = makeAuthenticate()
  const validationStub = makeValidation()
  const sut = new LoginController(authenticationStub, validationStub)
  return {
    sut,
    authenticationStub,
    validationStub
  }
}

